        public static string ConvertNumber(string number)
        {
            string isNegative = "";
            string word = "";
            try
            {

                number = Convert.ToDouble(number).ToString();

                if (number.Contains("-"))
                {
                    //isNegative = "Minus ";
                    number = number.Substring(1, number.Length - 1);
                }
                if (number == "0")
                {
                    word = "Zero Only";
                }
                else
                {
                    word = isNegative + ConvertToWords(number);
                }
            }
            catch (Exception ex)
            {
                Console.WriteLine(ex.Message);
            }
            return word;
